export function resolvePalette(settings: EditorSettings): Palette {
  if (settings.customPalette?.length) {
    return {
      id: "custom",
      name: "Custom",
      defaultColorMode: settings.preprocess.colorMode,
      colors: settings.customPalette.map((hex, index) =>
        color(`Custom ${index + 1}`, hex)
      ),
    }
  }

  return (
    PRESET_PALETTES.find((palette) => palette.id === settings.paletteId) ??
    PRESET_PALETTES[0]
  )
}

export function getEffectivePalette(
  palette: Palette,
  colorDepth: ColorDepth
): Palette {
  if (colorDepth.mode === "full" || palette.colors.length <= colorDepth.count) {
    return palette
  }

  return {
    ...palette,
    colors: palette.colors.slice(0, colorDepth.count),
  }
}

export function hexToRgb(hex: string): Rgb {
  const normalized = hex.replace("#", "")
  const full =
    normalized.length === 3
      ? normalized
          .split("")
          .map((part) => `${part}${part}`)
          .join("")
      : normalized

  return [
    Number.parseInt(full.slice(0, 2), 16),
    Number.parseInt(full.slice(2, 4), 16),
    Number.parseInt(full.slice(4, 6), 16),
  ]
}

function color(name: string, hex: string): PaletteColor {
  return {
    name,
    hex,
    rgb: hexToRgb(hex),
  }
}
